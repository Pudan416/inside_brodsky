        # –ü—Ä–æ–≤–µ—Ä—è–µ–º —Ç–µ–∫—É—â—É—é –ª–æ–∫–∞—Ü–∏—é
        current_loc = self.locations.get(self.current_location, {})
        connections = current_loc.get('connections', [])
        
        if destination not in connections:
            return f"–û—Ç—Å—é–¥–∞ –Ω–µ–ª—å–∑—è –ø–æ–ø–∞—Å—Ç—å –≤ –ª–æ–∫–∞—Ü–∏—é '{destination.replace('_', ' ')}'.\n–î–æ—Å—Ç—É–ø–Ω—ã–µ –Ω–∞–ø—Ä–∞–≤–ª–µ–Ω–∏—è: {', '.join([c.replace('_', ' ') for c in connections])}"
        
