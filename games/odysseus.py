            "–ó–≤–µ–∑–¥—ã —Å–∫–ª–∞–¥—ã–≤–∞—é—Ç—Å—è –≤ —É–∑–æ—Ä—ã, –∫–æ—Ç–æ—Ä—ã–µ –ø–æ–º–æ–≥–∞—é—Ç –æ–ø—Ä–µ–¥–µ–ª–∏—Ç—å –ø—É—Ç—å.",
