    def look_around(self) -> str:
        """–û—Å–º–æ—Ç—Ä–µ—Ç—å —Ç–µ–∫—É—â—É—é –ª–æ–∫–∞—Ü–∏—é"""
        location = self.locations.get(self.current_location, {})
        
        description = f"\n–¢—ã ‚Äî –û–¥–∏—Å—Å–µ–π, –Ω–∞—Ö–æ–¥–∏—à—å—Å—è –≤ –ª–æ–∫–∞—Ü–∏–∏: {self.current_location.replace('_', ' ')}.\n"
        description += f"{location.get('description', '–ù–µ–∏–∑–≤–µ—Å—Ç–Ω–æ–µ –º–µ—Å—Ç–æ')}\n\n"
        
        # –õ—é–¥–∏ –≤ –ª–æ–∫–∞—Ü–∏–∏
        npcs = location.get('npcs', [])
        if npcs:
            description += "–†—è–¥–æ–º —Å —Ç–æ–±–æ–π:\n"
            for npc in npcs:
                description += f"- {npc.replace('_', ' ').capitalize()}\n"
        else:
            description += "–ó–¥–µ—Å—å –Ω–∏–∫–æ–≥–æ –Ω–µ—Ç.\n"
        
        # –ü—Ä–µ–¥–º–µ—Ç—ã, –¥–æ—Å—Ç—É–ø–Ω—ã–µ –≤ –ª–æ–∫–∞—Ü–∏–∏
        items = location.get('items', [])
        if items:
            description += "\n–¢—ã –≤–∏–¥–∏—à—å –≤–æ–∫—Ä—É–≥:\n"
            for item in items:
                description += f"- {item}\n"
        else:
            description += "\n–ó–¥–µ—Å—å –Ω–µ—Ç –æ—Å–æ–±—ã—Ö –ø—Ä–µ–¥–º–µ—Ç–æ–≤.\n"
        
        # –í–æ–∑–º–æ–∂–Ω—ã–µ –Ω–∞–ø—Ä–∞–≤–ª–µ–Ω–∏—è
        connections = location.get('connections', [])
        if connections:
            description += "\n–û—Ç—Å—é–¥–∞ –º–æ–∂–Ω–æ –ø–æ–π—Ç–∏ –≤:\n"
            for conn in connections:
                description += f"- {conn.replace('_', ' ')}\n"
        
        description += f"\n–¢–≤–æ–∏ –ø–∞—Ä–∞–º–µ—Ç—Ä—ã:\n"
        description += f"- –ú—É–¥—Ä–æ—Å—Ç—å: {self.wisdom}/100\n"
        description += f"- –¢–æ—Å–∫–∞ –ø–æ –¥–æ–º—É: {self.homesickness}/100\n"
        description += f"- –ü—Ä–æ–≥—Ä–µ—Å—Å –ø—É—Ç–∏: {self.journey}/100\n"
        
